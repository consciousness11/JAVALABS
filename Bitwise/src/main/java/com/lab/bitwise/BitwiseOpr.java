package com.lab.bitwise;

/**
 *
 * @author Marut
 */
public class BitwiseOpr {
    public static void main(String[] args){
    int a=-7;
    int b=5;
    
    System.out.println("AND a&b= "+(a&b));
    System.out.println("OR a|b= "+(a|b));
    System.out.println("XOR a^b= "+(a^b));
    System.out.println("NOT ~a= "+~a);
    System.out.println("SHIFT LEFT a<<2= "+(a<<2));
    System.out.println("SHIFT RIGHT a>>1= "+(a>>2));
    System.out.println("UNSIGNED SHIFT RIGHT a>>>1= "+(a>>>2));
   
    
    
    
    }
}
